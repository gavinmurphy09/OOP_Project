    private void loadFile() {
      
        FileInputStream fStream;
        ObjectInputStream oStream;
        try{
            f = new File(busFile);
                    
            fStream = new FileInputStream(f);
                    
            oStream = new ObjectInputStream(fStream);
            //save the file to the arrayList
            //remind java what type of object we are getting from file
            //cast it o arrayList holding our library item obj
          
            elist=(ArrayList<Business>)oStream.readObject();
            objectTA.selectAll();
            objectTA.replaceSelection("");
            objectTA.append("sucessfully loaded data from "+busFile+ "\n");
        
            //System.out.println("file loaded    \n");
                   
            oStream.close();
        }catch(IOException | ClassNotFoundException e){
            System.out.println("error"+e +"\n");
            objectTA.append("no files to open\n");
        }
    }
    public void saveFile(){
        
        FileOutputStream fStream;
        ObjectOutputStream oStream;
        
        try{
            f = new File(busFile);
            fStream = new FileOutputStream(f);
            oStream = new ObjectOutputStream(fStream);
            //write to file
            oStream.writeObject(elist);
            oStream.close();
            objectTA.selectAll();
            objectTA.replaceSelection("");
            objectTA.append("sucessfully saved data to "+busFile+ "\n");
          
            //f.delete();
        }catch(IOException e){
            System.out.println("error="+e);
        }
       
    }
