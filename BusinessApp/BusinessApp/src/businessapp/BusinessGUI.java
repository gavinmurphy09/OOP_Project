    private void displayBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayBTNActionPerformed
        // TODO add your handling code here:
        String details;
        objectTA.selectAll();
        objectTA.replaceSelection("");
         for(int i = 0;i<elist.size();i++){
            Business temp;
            temp= elist.get(i);
            if(temp instanceof Restaurant){
                details= "Business: Restaurant "+" Name: "+temp.getBusinessName() +" Revenue: "+temp.getRevenue() +" units Sold: "+((Restaurant) temp).getUnitsSold()+" Distributor Cost: "+((Restaurant) temp).getDistributorCost()+" Compostable: "+((Restaurant)temp).getCompostable();
                
            }else if(temp instanceof Textile){
                details="Business: Textile "+" Name: "+temp.getBusinessName() +" Revenue: "+temp.getRevenue() +" items Sold: "+((Textile) temp).getItemsSold()+" Textile Cost: "+((Textile) temp).getTextileCost()+" Upgrade Cost: "+((Textile)temp).getUpgradeCost();
            }else if(temp instanceof Manufacturing){
                details="Business: Manufacturing "+" Name: "+temp.getBusinessName() +" Revenue: "+temp.getRevenue() +" units Sold: "+((Manufacturing) temp).getUnitsSold()+" Machinery Cost: "+((Manufacturing) temp).getMachineryCost()+" Compostable: "+((Manufacturing)temp).getCompostable();
            }else{
                details="Name: "+temp.getBusinessName() +" Revenue: "+temp.getRevenue();
            }
            objectTA.append(details+"\n");
            //JOptionPane.showMessageDialog(null,temp.printDetails());
        
        }
    }//GEN-LAST:event_displayBTNActionPerformed

    private void searchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTNActionPerformed
        // TODO add your handling code here:
        objectTA.selectAll();
        objectTA.replaceSelection("");
        if(elist.isEmpty()){
            JOptionPane.showMessageDialog(null, "No one in system");
        }else{
            String searchTerm = JOptionPane.showInputDialog(null, "Enter Name");
            for(Business temp:elist){
                if(temp.getBusinessName().equalsIgnoreCase(searchTerm)){
                    objectTA.append(temp.toString()+"\n");
                }
            }
        }
    }//GEN-LAST:event_searchBTNActionPerformed

    private void delBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delBTNActionPerformed
        // TODO add your handling code here:
        objectTA.selectAll();
        objectTA.replaceSelection("");
        if(elist.isEmpty()){
            JOptionPane.showMessageDialog(null, "No one in system");
        }else{
            String searchTerm = JOptionPane.showInputDialog(null, "Enter Name");
            for(int i=0; i<elist.size();i++){
                Business temp = elist.get(i);
                if(temp.getBusinessName().equalsIgnoreCase(searchTerm)){
                    elist.remove(i);
                    objectTA.append(temp.getBusinessName()+ " Removed" +"\n");
                    //JOptionPane.showMessageDialog(null,temp.getBusinessName()+ " Removed");
                }
            }
        }
        
    }//GEN-LAST:event_delBTNActionPerformed

