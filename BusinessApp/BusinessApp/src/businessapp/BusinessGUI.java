    private void computeBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_computeBTNActionPerformed
        // TODO add your handling code here:
        double plastic=0, cost=0;
        int esg =0; 
        if(elist.isEmpty()){
            JOptionPane.showMessageDialog(null, "No one in system");
        }else{
            String searchTerm = JOptionPane.showInputDialog(null, "Enter Name");
        
           
                for(int i = 0;i<elist.size();i++){
                    Business temp;

                    temp= elist.get(i);
                    if(temp.getBusinessName().equalsIgnoreCase(searchTerm)){
                        if(temp instanceof Restaurant){
                            plastic=((Restaurant) temp).plasticCalc();
                            cost=temp.costCalc();
                        }else if(temp instanceof Textile){
                            plastic=((Textile) temp).plasticCalc();
                            cost=temp.costCalc();
                            esg=((Textile) temp).esgCalc();
                        }else if(temp instanceof Manufacturing){
                            cost=temp.costCalc();
                            esg=((Manufacturing) temp).esgCalc();
                            plastic=((Manufacturing) temp).plasticCalc();
                        }else{
                            cost=temp.costCalc();
                            esg=temp.esg();
                            plastic=0;
                        }
                    }
            
                }
            esgLBL.setText("ESG Score "+"\n"+Integer.toString(esg));
            plasticLBL.setText("Plastic Removed \n"+Double.toString(plastic)+"Kg");
            costLBL.setText("Cost Value \n"+Double.toString(cost)+"â‚¬");
        }
    }//GEN-LAST:event_computeBTNActionPerformed

